var images = {
    list: {},
    load: function(name, src) {
        this.list[name] = new Image();
        this.list[name].ready = false;
        this.list[name].onload = function(img) {
            img.ready = true;
        }
        this.list[name].src = src;
    },
    is_ready: function(name) {
        return this.list[name].ready;
    },
    get: function(name) {
        return this.list[name];
    }
};
